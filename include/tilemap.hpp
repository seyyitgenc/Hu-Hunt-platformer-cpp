void load(std::vector<rect> &ColRect)
{
    int index = 0;
    ColRect.push_back({{gridSize, gridSize}, {0, 0}, {0.0f, 0.0f}, sf::Color::Red});
    for (int i = 0; i < 32; i++)
    {
        for (int j = 0; j < 20; j++)
        {
            if (map[i + j * 32] == 1)
            {
                ColRect.push_back({{gridSize, gridSize}, {(float)i * 32.0f, (float)j * 32.0f}, {0.0f, 0.0f}, sf::Color::Yellow});
                index++;
            }
            if (map[i + j * 32] == 2)
            {
                ColRect.push_back({{gridSize, gridSize}, {(float)i * 32.0f, (float)j * 32.0f}, {0.0f, 0.0f}, sf::Color::Green});
                index++;
            }
            if (map[i + j * 32] == 3)
            {
                movingPlatforms.push_back({{{gridSize, gridSize}, {(float)i * 32.0f, (float)j * 32.0f}, {100.0f, 100.0f}, sf::Color::White}, index});
                ColRect.push_back({{gridSize, gridSize}, {(float)i * 32.0f, (float)j * 32.0f}, {0.0f, 0.0f}, sf::Color::White});
                index++;
            }
        }
    }
}
